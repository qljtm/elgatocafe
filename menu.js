const linkscat = document.querySelectorAll(".sidebar a");

window.addEventListener('DOMContentLoaded', () => {
    displayAll(menuData);
});

linkscat.forEach((links) => {
    links.addEventListener("click", (e) => {
        linkscat.forEach(link => link.classList.remove('active'));
        e.target.classList.add('active');

        const category = e.target.dataset.id;
        const menuGrid = document.querySelector('.menu-grid');
        menuGrid.classList.add('fading-out');
        menuGrid.classList.remove('fading-in');

        setTimeout(() => {
            const menuCateg = menuData.filter(function (data) {
                if (data.category === category || category === 'All') {
                    return data;
                }
            });

            if (category === 'All') {
                displayAll(menuData);
            } else {
                displayAll(menuCateg);
            }

            menuGrid.classList.remove('fading-out');
            menuGrid.classList.add('fading-in');
        }, 500);
    });
});

function displayAll(AllMenu) {
    let displayMenu = AllMenu.map(function(get_items){
        return `
        <div class="menu-item" data-category="${get_items.category}">
            <img src="${get_items.img}" alt="${get_items.product}">
            <h4>${get_items.product}</h4>
            <hr>
            <p>${get_items.price}</p>
        </div>
    `    
    }).join('');
    document.getElementsByClassName('menu-grid')[0].innerHTML = displayMenu;
}